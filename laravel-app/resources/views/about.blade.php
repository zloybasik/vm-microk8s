    <ul>
        <li>Построение маршрутов и навигация</li>
        <li>Поиск объектов и адресов</li>
        <li>Визуализация данных (например, пробки, погода, демография)</li>
        <li>Геоаналитика для бизнеса и науки</li>
    </ul>
    <p>
